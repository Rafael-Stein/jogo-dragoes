  <h2>Criação de Personagem</h2>
    <p>O jogo se inicia, em uma das mais importantes etapas, a criação do seu personagem. O mestre irá definir que tipo de aventura irá propor, e através dessa decisão, você e seus amigos irão distribuir os pontos que receberam para criar e melhorar seus personagens, sejam elas adquirindo mais atributos ou habilidades, ou mesmo, que grupo de vantagens e desvantagens terão.</p>
    <p>Em uma campanha normal em “OS REINOS DE ASPEN”, 12 pontos iniciais de atributos são dados aos jogadores para distribuir aos seus personagens, guarde esse número na cabeça. Se o mestre quiser aumentar, recomenda-se um aumento de seis em seis pontos, ficando 18, 24, etc. O jogador irá escolher primeiramente a raça ao qual irá querer jogar, nesse ponto o mestre também tem participação, pois pode restringir ou liberar alguma classe de poder específica (ver mais adiante). Como nesse Módulo Básico, vamos tratar também do primeiro cenário de jogo, “Os Sete Paraísos Celestiais”, é pertinente que possamos utilizar qualquer raça comum até nível mágico e usar qualquer uma das sete variações da raça de Amazonas, que são seres exclusivos de Classe de Poder Celestial, para nosso modelo de criação, usaremos as Amazonas de Prata, que vamos chamar de Auris.</p>
    <h2>Qual a diferença entre % e pts%?</h2>
    <p>Em diversos trechos deste livro haverá referência de % e de pts%, mas qual a sua diferença? A diferença entre eles é que quando se trata de %, é a porcentagem aplicada direta, seja para aumentar ou reduzir um valor. Então se no texto indica, por exemplo, que numa determinada habilidade, que é 60%, haverá um aumento de 10%, você deve adicionar 6% nela, ficando um total de 66%, se fosse para reduzir 5% nessa mesma habilidade, você reduziria 3%, ficando um valor total de 57%, que pode ser referendada também apenas como 57.</p>
    <p>Já quando se trata de pts%, ela implica a soma ou dedução direta do valor sobre outro valor existente. No exemplo acima, a habilidade de 60% vai receber um acréscimo de 10pts%, então ela vai adicionar 10pts nessa habilidade, ficando em 70%, se fosse para deduzir 5pts%, ela ficaria então como 55%.</p>
    <p>Todos os valores de testes de atributos e habilidades são baseados em um valor de porcentagem. E por esse motivo será utilizado um dado poliedro de 100 faces ou podem ser usados 2 dados de 10 faces (sendo usado 1 dado como dezena e o outro como unidade) ou ainda o uso de alguma ferramenta que possa gerar dados aleatórios de 01 a 100. </p>
    <p>A porcentagem resultada na rolagem terá 2 relevâncias. Uma para determinar o valor correspondente ao valor da habilidade em si e o outro para avaliar se o teste teve ou não sucesso, ou uma falha crítica. Maiores detalhes serão descritos no capítulo que abordaremos sobre fazendo os testes de habilidades.</p>
    <h2>Testes de Habilidade:</h2>
    <p>Em diversos momentos durante o jogo, haverá a necessidade de uma comparação entre “o que o personagem está querendo fazer” com “se ele teve sucesso em fazer”, para isso será necessário um teste de habilidade. Os personagens principalmente no início, terão valores considerados medianos em suas habilidades, variando entre 50% e 65%, em seus testes, será sempre rolado um dado de 100 faces (ou 2 de 10 faces), por outro lado, personagens mais avançados terão valores que superam a casa dos 100%, uma vez que não há limite para um valor de habilidade, poderia um personagem extremamente forte ter uma habilidade em mais de 300%. Olhando assim se torna fácil perceber que um personagem inicial não vai êxito na maioria das vezes, já o personagem mais experiente, jamais vai falhar. Para que exista um equilíbrio, no sistema de Os Reinos de Aspen, o valor de habilidade dos personagens possui uma margem de sucessos e falhas diferente ao do Nível de habilidade Efetivo.</p>
